						<?php # NAMING CONVENTIONS ?><!--{{{-->
						<section id="naming-conventions" class="accordion-group">
							<a class="btn btn-block accordion-toggle" data-toggle="collapse" data-parent="#accordion" href="#naming-conventions-c">
								<h2>Naming Conventions</h2>
							</a>

							<div id="naming-conventions-c" class="accordion-body collapse">
								<div class="accordion-inner">
									<p>
										To increase productivity by avoiding the kind of confusion that non-uniformity brings, many
										design/development shops establish style guides and coding conventions. For the sake of this course
										and to avoid complications, adhere to the following naming conventions strictly unless told otherwise.
									</p>

									<h3>Case Sensitivity</h3>
									<p>
										The Microsoft Windows platform is <em>case insensitive</em>. Where as Unix (Mac, Linux, Android, etc.)
										platforms are <em>case sensitive</em>. The majority of web servers today run on a Unix-like platforms, so
										naming conventions for web development projects are influenced by such.
									</p>

									<p class="important">
										<span class="label label-important"><i class="icon-exclamation-sign icon-white"></i> Convention </span>
										<strong>All file and folder names are to be in lower-case.</strong>
									</p>

									<div class="row-fluid">
										<div class="span6"><img src="../../img/case-no.jpg" alt="Illustration of what to avoid"></div>
										<div class="span6"><img src="../../img/case-yes.jpg" alt="Illustration of what to employ"></div>
									</div>

									<h3>Spaces</h3>
									<p>
										Operating System platforms (Win, Mac, Lin, etc.) have two interface types. The first is a low-level
										CLI (Command Line Interface) where you type in commands at a prompt. The second is a high-level GUI
										(Graphic User Interface) where you point with a cursor and click at user-interface widgets. GUIs
										usually are more human friendly and work intuitively with spaces in filenames. However, at the low
										system level, CLIs require double quotes or escape characters and such to deal with spaces in
										filenames. Development lives at the system level, so it's common practice to avoid spaces in file
										names.
									</p>

									<p class="important">
										<span class="label label-important"><i class="icon-exclamation-sign icon-white"></i> Convention </span>
										<strong>All file and folder names are to use dashes in place of spaces.</strong>
									</p>

									<div class="row-fluid">
										<div class="span6"><img src="../../img/space-no.jpg" alt="Illustration of what to avoid"></div>
										<div class="span6"><img src="../../img/space-yes.jpg" alt="Illustration of what to employ"></div>
									</div>

									<p>
										<span class="label label-info"><i class="icon-eye-open icon-white"></i> FYI </span>
										These naming conventions do not only apply to files and folders, but to class and ID names as
										well, which will be covered later in the course.
									</p>
								</div><!-- accordion-inner -->
							</div>
						</section>


